package nishat;

public class DataTypes {

public static void main(String[] args){

 ///Primitive Data Types

    byte num0 = 100;
    int num1 = 500;
    float num2 = 20.5f;
    double num3 = 3.14159265;
    char num4 = 'A';
    boolean num5 = false;

    System.out.println("Byte: "+num0);
    System.out.println("Integer: "+num1);
    System.out.println("Float: "+num2);
    System.out.println("Double: " +num3);
    System.out.println("Character: "+num4);
    System.out.println("Boolean: "+num5);



    System.out.println("Byte: "+num0+",\nInteger: "+num1+",\nFloat: "+num2+" ,\nDouble: " +num3+" ,\nCharacter: "+num4+" ,\nBoolean: "+num5);


    ///Non-Primitive Data Types

    String name = "Esrat";
    int[] num = {1,2,3,5,8,9};
    System.out.println("Hello " +name);
    System.out.println("Numbers:" +num[2]);




}

}
